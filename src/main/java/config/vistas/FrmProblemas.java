    private void guardarProblema() {
        try {
        
            String descripcion = txtDesc.getText().trim(); 
            String estado = cmbEstado.getSelectedItem().toString().trim();
            Date fechaInicio = (Date) txtFchInicio.getDate();
            Date fechaFin = (Date) txtFchFin.getDate();

            if (descripcion.isEmpty() || fechaFin == null || fechaInicio == null || this.cliente == null) {
                JOptionPane.showMessageDialog(
                        this,
                        "Todos los campos son obligatorios.",
                        "Error",
                        JOptionPane.WARNING_MESSAGE
                );
                return;
            }

            java.sql.Date sqlDateInicio = new java.sql.Date(fechaInicio.getTime());
            java.sql.Date sqlDateFin = new java.sql.Date(fechaFin.getTime());
            
            boolean exito;
            if (btnGuardar.getText().equals("GUARDAR")) {

                // Ahora regresa boolean
                        exito = pController.agregarProblema(
                        sqlDateInicio,
                        sqlDateFin,
                        estado,
                        this.cliente.getIdCliente(),
                        descripcion
                );

                if (exito) {
                    JOptionPane.showMessageDialog(this, "Problema guardado correctamente.");
                }
            }else{
                int id = Integer.parseInt(txtID.getText());
                exito = pController.actualizarProblema(id, sqlDateFin, sqlDateFin, estado, this.cliente.getIdCliente());
                if (exito){
                    JOptionPane.showMessageDialog(this, "Problema actualizado correctamente");
                }
            }
            if (exito){
                cargarProblemas();
                limpiarCampos();
            } else {
                    JOptionPane.showMessageDialog(
                            this,
                            "Ocurrió un error al guardar el problema.",
                            "Error",
                            JOptionPane.ERROR_MESSAGE
                    );
                }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(
                    this,
                    "Error: " + e.getMessage(),
                    "Error",
                    JOptionPane.ERROR_MESSAGE
            );
        }
    }
    
