    private void cargarDatos(){
        int fila = tblProblemas.getSelectedRow();
        if (fila >= 0 ){
            txtID.setText(tblProblemas.getValueAt(fila, 0).toString());
            
            txtFchInicio.setDate((Date) tblProblemas.getValueAt(fila, 1));
            txtFchFin.setDate((Date) tblProblemas.getValueAt(fila, 2));

            cmbEstado.setSelectedItem(tblProblemas.getValueAt(fila, 3).toString());
            
            txtCliente.setText(tblProblemas.getValueAt(fila, 4).toString());
            txtDesc.setText(tblProblemas.getValueAt(fila, 5).toString());
            
            btnGuardar.setText("Actualizar");
            
        }
    }
     
    private void limpiarCampos() {
        txtID.setText("0");
        txtDesc.setText("");
        txtFchInicio.setDate(null);
        txtFchFin.setDate(null);
        cmbEstado.setSelectedIndex(0);
        txtCliente.setText("");
        this.cliente = null; 
        btnGuardar.setText("GUARDAR");
    
    }
    
    
    
    
    
